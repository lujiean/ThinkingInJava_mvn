package com.thinkinginjava.operators;

/*
 * Exercise 4: (2) Write a program that calculates velocity using a constant distance and a constant time.
 */
public class Exercise4 {

	public static final double distance = 100.0d;
	public static final double time = 2.0d;
	
	public static double CalVelocity() {
		return distance/time;
	}
}
